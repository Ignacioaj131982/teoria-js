//Dato del tipo number
let edad = 41;

console.log(edad);

//Dato del tipo number con decimales

let precio = 150.8;

console.log(precio);

//dato del tipo string

let nombre = "Nacho";

console.log(nombre);

//dato del tipo string con comillas simples

let apellido = 'Arevalo';
console.log(apellido);

//dato booleano con valor true

let verdadero = true;
console.log(verdadero);

//dato booleano con valor false

let falso = false;
console.log(falso);

//dato del tipo NanN -Not a Number-

let notANumber = NaN;
console.log(notANumber);

//dato del tipo null

let nulo = null;
console.log(nulo);

//dato undefined
let sinValor = "Sin Valor";
console.log(sinValor);

//dato del tipo objeto literal
let persona = {
    nombre: 'Nacho', 
    peso: 97};
console.log(persona);

//dato del tipo array
let paises = ['Argentina', 'Venezuela', 'Colombia'];
console.log(paises);
