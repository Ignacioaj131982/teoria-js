console.log(6>=6);

// Menor que

console.log(5< 3);

// Menor o igual que

console.log(5<=5);

// AND &&

console.log(true && true); //ambas condiciones deben ser verdaderas

console.log(false && true); //si una de las condiciones es falsa va a dar false

// OR ||

console.log(true || false); //si una de las condiciones es true va a dar true

